-- =====================================================================
-- КРОК 1: СТВОРЕННЯ ТАБЛИЦІ ДЛЯ РЕЗУЛЬТАТІВ
-- =====================================================================
-- (Якщо видасть помилку при DROP - ігноруй, це нормально)
DROP TABLE monthly_salaries;

-- Створюємо зарплатну відомість, куди процедури будуть записувати гроші
CREATE TABLE monthly_salaries (
    salary_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id NUMBER,                           -- ID працівника
    work_month NUMBER,                            -- За який місяць зарплата
    total_payout NUMBER(10,2),                    -- Сума до виплати
    payment_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP -- Дата нарахування
);


-- =====================================================================
-- КРОК 2: ПРОЦЕДУРА 1 (Індивідуальне нарахування)
-- =====================================================================
-- Виділи від CREATE до слеша (/) і натисни Ctrl+Enter
CREATE OR REPLACE PROCEDURE calc_employee_salary (
    p_emp_id IN NUMBER,
    p_month IN NUMBER,
    p_hours_worked IN NUMBER,
    p_hourly_rate IN NUMBER
) AS
    v_total_salary NUMBER(10,2);
BEGIN
    -- Бізнес-логіка: Обчислюємо зарплату (Відпрацьовані години * Ставку)
    v_total_salary := p_hours_worked * p_hourly_rate;

    -- Записуємо обчислену суму в нашу відомість
    INSERT INTO monthly_salaries (employee_id, work_month, total_payout)
    VALUES (p_emp_id, p_month, v_total_salary);

    COMMIT;
END;
/


-- =====================================================================
-- КРОК 3: ПРОЦЕДУРА 2 (Масове нарахування - ВИКЛИКАЄ ПЕРШУ)
-- =====================================================================
-- Виділи від CREATE до слеша (/) і натисни Ctrl+Enter
CREATE OR REPLACE PROCEDURE calc_all_salaries (
    p_month IN NUMBER
) AS
BEGIN
    -- Тут ми масово викликаємо ПЕРШУ процедуру для різних працівників.
    -- (Передаємо: ID працівника, Місяць, Години, Ставку)

    calc_employee_salary(1, p_month, 160, 25); -- Працівник 1 (Frontend)
    calc_employee_salary(2, p_month, 140, 30); -- Працівник 2 (Backend)
    calc_employee_salary(3, p_month, 100, 50); -- Працівник 3 (DevOps)
    calc_employee_salary(4, p_month, 160, 40); -- Працівник 4 (Project Manager)
END;
/


-- =====================================================================
-- КРОК 4: ЗАПУСК ТА ПЕРЕВІРКА (Фінал)
-- =====================================================================
-- 4.1. Викликаємо головну масову процедуру (Нараховуємо ЗП за 3-й місяць - Березень)
-- Виділи від BEGIN до слеша (/) і натисни Ctrl+Enter
BEGIN
    calc_all_salaries(3);
END;
/

-- 4.2. ПЕРЕВІРКА: Дивимось у нашу відомість! 
-- (Тут мають з'явитися 4 працівники з готовими сумами зарплат)
SELECT * FROM monthly_salaries;